from transformers import BertPreTrainedModel, BertModel
from transformers.modeling_outputs import QuestionAnsweringModelOutput
import torch.nn as nn
import torch.nn.functional as F

class WeightedAttention(nn.Module):
    def __init__(self, input_dim, hidden_dim):
        super(WeightedAttention, self).__init__()
        self.hidden_dim = input_dim
        self.projection = nn.Sequential(
            nn.Linear(input_dim, hidden_dim),
            nn.ReLU(True),
            nn.Linear(hidden_dim, 1)
        )

    def forward(self, inputs):
        # (B, L, H) -> (B , L, 1)
        energy = self.projection(inputs)
        weights = F.softmax(energy.squeeze(-1), dim=1)
        # (B, L, H) * (B, L, 1) -> (B, H)
        outputs = (inputs * weights.unsqueeze(-1)).sum(dim=1)
        return outputs, weights

class BiLSTMAttention(nn.Module):
    def __init__(self, lstm_hidden_dim):
        super(BiLSTMAttention, self).__init__()

        # self.embedding = nn.Embedding.from_pretrained(word_embedding_lookup_table, padding_idx=0)
        self.rnn = nn.LSTM(lstm_hidden_dim,
                           lstm_hidden_dim // 2,
                           bidirectional=True,
                           batch_first=True)

        self.attention = WeightedAttention(lstm_hidden_dim, lstm_hidden_dim * 2)
    
    def forward(self, inputs, lengths=None):

        # x = nn.utils.rnn.pack_padded_sequence(x, lengths, batch_first=True)
        out, _ = self.rnn(inputs)
        # out = nn.utils.rnn.pad_packed_sequence(out, batch_first=True)[0]

        # out shape is (batch, clause_len, lstm_hidden_dim)
        out, _ = self.attention(out)
        # out shape is (batch, lstm_hidden_dim)
        return out


class BertForQuestionAnswering_Bilstm(BertPreTrainedModel):
    _keys_to_ignore_on_load_unexpected = [r"pooler"]

    def __init__(self, config):
        super().__init__(config)
        self.num_labels = config.num_labels

        self.bert = BertModel(config, add_pooling_layer=False)
        self.encoder = BiLSTMAttention(config.hidden_size)

        # rnn_dim = config.hidden_size // 2
        # self.rnn = nn.LSTM(bidirectional=True, num_layers=1, input_size=config.hidden_size, hidden_size=rnn_dim, batch_first=True)
        
        # out_dim = rnn_dim * 2
        out_dim = config.hidden_size
    
        self.dropout = nn.Dropout(0.1)
        self.qa_outputs = nn.Linear(out_dim, config.num_labels)

        self.init_weights()

    def forward(
        self,
        input_ids=None,
        attention_mask=None,
        token_type_ids=None,
        position_ids=None,
        head_mask=None,
        inputs_embeds=None,
        start_positions=None,
        end_positions=None,
        output_attentions=None,
        output_hidden_states=None,
        return_dict=None,
    ):
        r"""
        start_positions (:obj:`torch.LongTensor` of shape :obj:`(batch_size,)`, `optional`):
            Labels for position (index) of the start of the labelled span for computing the token classification loss.
            Positions are clamped to the length of the sequence (:obj:`sequence_length`). Position outside of the
            sequence are not taken into account for computing the loss.
        end_positions (:obj:`torch.LongTensor` of shape :obj:`(batch_size,)`, `optional`):
            Labels for position (index) of the end of the labelled span for computing the token classification loss.
            Positions are clamped to the length of the sequence (:obj:`sequence_length`). Position outside of the
            sequence are not taken into account for computing the loss.
        """
        return_dict = return_dict if return_dict is not None else self.config.use_return_dict

        outputs = self.bert(
            input_ids,
            attention_mask=attention_mask,
            token_type_ids=token_type_ids,
            position_ids=position_ids,
            head_mask=head_mask,
            inputs_embeds=inputs_embeds,
            output_attentions=output_attentions,
            output_hidden_states=output_hidden_states,
            return_dict=return_dict,
        )

        sequence_output = outputs[0]
        # sequence_output, _ = self.rnn(sequence_output)
        print("#"*20, sequence_output.shape)
        sequence_output = self.encoder(sequence_output)
        print("#"*20, sequence_output.shape)
        # sequence_output = sequence_output.view(-1, 512, self.config.hidden_size)
        
        # sequence_output = self.dropout(sequence_output)
        
        logits = self.qa_outputs(sequence_output)
        start_logits, end_logits = logits.split(1, dim=-1)
        start_logits = start_logits.squeeze(-1)
        end_logits = end_logits.squeeze(-1)

        total_loss = None
        if start_positions is not None and end_positions is not None:
            # If we are on multi-GPU, split add a dimension
            if len(start_positions.size()) > 1:
                start_positions = start_positions.squeeze(-1)
            if len(end_positions.size()) > 1:
                end_positions = end_positions.squeeze(-1)
            # sometimes the start/end positions are outside our model inputs, we ignore these terms
            ignored_index = start_logits.size(1)
            start_positions.clamp_(0, ignored_index)
            end_positions.clamp_(0, ignored_index)

            loss_fct = nn.CrossEntropyLoss(ignore_index=ignored_index)
            start_loss = loss_fct(start_logits, start_positions)
            end_loss = loss_fct(end_logits, end_positions)
            total_loss = (start_loss + end_loss) / 2

        if not return_dict:
            output = (start_logits, end_logits) + outputs[2:]
            return ((total_loss,) + output) if total_loss is not None else output

        return QuestionAnsweringModelOutput(
            loss=total_loss,
            start_logits=start_logits,
            end_logits=end_logits,
            hidden_states=outputs.hidden_states,
            attentions=outputs.attentions,
        )



